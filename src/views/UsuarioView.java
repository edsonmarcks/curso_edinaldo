    private void validarCampos() throws Exception {
        if (jTextFieldUsuario.getText() == null || jTextFieldUsuario.getText().trim().equals("") || jTextFieldUsuario.getText().isEmpty()) {
            throw new Exception("O campo usuário não pode ser vazio!");
        }
        if (jTextFieldUsuario.getText().contains(" ")) {
            throw new Exception("O nome do usuário não pode ter espaços");
        }
        if (jPasswordFieldSenha.getPassword().length < 8) {
            throw new Exception("A senha deve ter no minimo 8 caracteres!");
        }
    }

    private void limpar() {
        jTextFieldUsuario.setText(null);
        jPasswordFieldSenha.setText(null);
    }

