    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {

            int resp = JOptionPane.showConfirmDialog(rootPane, "Confirmar o cadastro do usuário?", title, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (resp == JOptionPane.YES_OPTION) {
                validarCampos();
                usuario = new Usuario();
                usuario.setNome(jTextFieldUsuario.getText());
                usuario.setSenha(String.valueOf(jPasswordFieldSenha.getPassword()));
                usuario.setPermissaoUsuario((Permissao) cbPermissoes.getSelectedItem());

                if (dao.salvar(usuario)) {
                    JOptionPane.showMessageDialog(rootPane, "Usuário cadastrado com sucesso!", title, JOptionPane.INFORMATION_MESSAGE);
                    usuarioTableModel.adicionar(usuario);
                    usuario = null;
                    limpar();
                } else {
                    if (TransportMessage.COD_ERROR == 1062) {
                        JOptionPane.showMessageDialog(rootPane, "O nome de usuário já está em uso!.", title, JOptionPane.WARNING_MESSAGE);
                        jTextFieldUsuario.requestFocus();
                        return;
                    }
                    JOptionPane.showMessageDialog(rootPane, "Desculpe não foi possível salvar o usuário.", title, JOptionPane.WARNING_MESSAGE);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Desculpe!\n" + e.getMessage(), "Ah! Não!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        limpar();
        usuario=null;
        jTextFieldUsuario.requestFocus();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void jTextFieldPesquisarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarCaretUpdate
        if(jTextFieldPesquisar.getText().length() > 3){
            usuarioTableModel.setLista(dao.buscarPorNome(jTextFieldPesquisar.getText()));
        }
        else{
            usuarioTableModel.resetLista();
        }
    }//GEN-LAST:event_jTextFieldPesquisarCaretUpdate


